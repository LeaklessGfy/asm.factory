SECTION .text

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
global main
main:
	call read_int
	mov [input1], eax
	call read_int
	cmp eax, [input1]
	jg printer
	mov eax, [input1]
	printer:
	   	call print_int
		call print_nl

    	mov ebx, 0
    	mov eax, 1
    	int 0x80
